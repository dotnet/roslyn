sb.AppendLine();
sb.AppendLine("## Summary");
sb.AppendLine();
sb.AppendLine("The frequency-based hash function, trained on Roslyn's own identifier corpus:");
sb.AppendLine();
sb.AppendLine($"- **Bloom filter storage**: {100.0 * (totalFreq - totalCrc) / totalCrc:+0.0;-0.0}% ({(totalFreq >= totalCrc ? "+" : "")}{FKB(totalFreq - totalCrc)})");
sb.AppendLine($"- **Distinct n-grams (index time)**: {100.0 * (globalFreqAllLengthDist.Values.Sum() - globalCrcAllLengthDist.Values.Sum()) / globalCrcAllLengthDist.Values.Sum():+0.0;-0.0}%");
sb.AppendLine($"- **Covering n-gram count (query time)**: {100.0 * (globalFreqCoveringCount - globalCrcCoveringCount) / globalCrcCoveringCount:+0.0;-0.0}% (more = more Bloom filter probes per query)");
sb.AppendLine($"- **Mean covering length**: {crcMeanCover:F2} → {freqMeanCover:F2} ({freqMeanCover - crcMeanCover:+0.00;-0.00} chars)");
sb.AppendLine($"- **Selective (>= 5 char) covering share**: {100.0 * crcSel5 / globalCrcCoveringCount:F1}% → {100.0 * freqSel5 / globalFreqCoveringCount:F1}%");
sb.AppendLine();
sb.AppendLine("### Interpretation");
sb.AppendLine();
sb.AppendLine("The Cursor/Blackbird frequency-based approach was designed for **arbitrary code text** where");
sb.AppendLine("you have a mix of operators, syntax, and varying character distributions. For **identifiers**");
sb.AppendLine("specifically, the common bigrams (`er`, `in`, `st`, `te`, `on`) ARE the identifier — they are");
sb.AppendLine("the substance we want to match against. Making them interior (low-weight) causes the algorithm");
sb.AppendLine("to place boundaries at common positions, fragmenting identifiers into many short covering");
sb.AppendLine("n-grams rather than fewer long ones.");
sb.AppendLine();
sb.AppendLine("The CRC hash, being content-agnostic, distributes boundaries more evenly across identifiers,");
sb.AppendLine("which paradoxically produces better covering sets for identifier-focused use cases.");

