// --- Hash functions ---

const ulong Mul1 = 0xc6a4a7935bd1e995UL;
const ulong Mul2 = 0x228876a7198b743UL;

uint CrcHashBigram(ReadOnlySpan<char> text, int pos)
{
    var a = (ulong)text[pos] * Mul1 + (ulong)text[pos + 1] * Mul2;
    return (uint)(a + (~a >> 47));
}

#region Frequency weights (generated by BigramFrequencyAnalyzer from Roslyn's identifiers)

const string Alphabet = "abcdefghijklmnopqrstuvwxyz0123456789_";
const int AlphabetSize = 37;

int FreqCharIndex(char c)
{
    if (c is >= 'a' and <= 'z') return c - 'a';
    if (c is >= '0' and <= '9') return 26 + (c - '0');
    if (c == '_') return 36;
    return -1;
}

uint FreqHashBigram(ReadOnlySpan<char> text, int pos)
{
    var idx1 = FreqCharIndex(text[pos]);
    var idx2 = FreqCharIndex(text[pos + 1]);
    if (idx1 >= 0 && idx2 >= 0)
        return FrequencyWeights[idx1 * AlphabetSize + idx2];

    var a = (ulong)text[pos] * Mul1 + (ulong)text[pos + 1] * Mul2;
    return (uint)(a + (~a >> 47));
}

