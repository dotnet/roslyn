// --- Generic n-gram algorithm (parameterized by mode: 0=CRC, 1=Freq) ---

const int CRC = 0, FREQ = 1;

uint Hash(ReadOnlySpan<char> text, int pos, int mode)
    => mode == CRC ? CrcHashBigram(text, pos) : FreqHashBigram(text, pos);

List<(int start, int length)> BuildAllNgrams(string text, int mode)
{
    var results = new List<(int start, int length)>();
    if (text.Length < 3) return results;

    var stack = new List<(uint hash, int pos)>();
    for (var i = 0; i + 2 <= text.Length; i++)
    {
        var hash = Hash(text, i, mode);
        while (stack.Count > 0 && hash > stack[^1].hash)
        {
            results.Add((stack[^1].pos, i + 2 - stack[^1].pos));
            while (stack.Count > 1 && stack[^1].hash == stack[^2].hash)
                stack.RemoveAt(stack.Count - 1);
            stack.RemoveAt(stack.Count - 1);
        }
        if (stack.Count > 0)
            results.Add((stack[^1].pos, i + 2 - stack[^1].pos));
        stack.Add((hash, i));
    }
    return results;
}

List<(int start, int length)> BuildCoveringNgrams(string text, int mode, int maxLen = 16)
{
    var results = new List<(int start, int length)>();
    if (text.Length < 3) return results;

    var deque = new LinkedList<(uint hash, int pos)>();
    for (var i = 0; i + 2 <= text.Length; i++)
    {
        var hash = Hash(text, i, mode);
        if (deque.Count > 1 && i - deque.First!.Value.pos + 3 >= maxLen)
        {
            results.Add((deque.First.Value.pos, deque.First.Next!.Value.pos + 2 - deque.First.Value.pos));
            deque.RemoveFirst();
        }
        while (deque.Count > 0 && hash > deque.Last!.Value.hash)
        {
            if (deque.First!.Value.hash == deque.Last.Value.hash)
            {
                results.Add((deque.Last.Value.pos, i + 2 - deque.Last.Value.pos));
                while (deque.Count > 1)
                {
                    var lp = deque.Last.Value.pos + 2;
                    deque.RemoveLast();
                    results.Add((deque.Last!.Value.pos, lp - deque.Last.Value.pos));
                }
            }
            deque.RemoveLast();
        }
        deque.AddLast((hash, i));
    }
    while (deque.Count > 1)
    {
        var lp = deque.Last!.Value.pos + 2;
        deque.RemoveLast();
        results.Add((deque.Last!.Value.pos, lp - deque.Last.Value.pos));
    }
    return results;
}

// --- Bloom filter sizing (matches Roslyn's BloomFilter.ComputeM) ---

const double FPR = 0.0001;

int BloomBytes(int n)
{
    if (n <= 0) return 0;
    var m = Math.Max(1, (int)Math.Ceiling(n * Math.Log(FPR) / Math.Log(1.0 / Math.Pow(2.0, Math.Log(2.0)))));
    return ((m + 7) & ~7) / 8;
}

