Console.Error.WriteLine($"Scanning .cs files under {rootDir}...");
var docIdentifierSets = new List<(string file, List<string> lowered)>();
var totalFiles = 0;

foreach (var file in Directory.EnumerateFiles(rootDir, "*.cs", SearchOption.AllDirectories))
{
    if (file.Contains($"{Path.DirectorySeparatorChar}obj{Path.DirectorySeparatorChar}") ||
        file.Contains($"{Path.DirectorySeparatorChar}bin{Path.DirectorySeparatorChar}"))
        continue;

    var text = File.ReadAllText(file);
    var tree = CSharpSyntaxTree.ParseText(text);
    var ids = new HashSet<string>(StringComparer.Ordinal);
    foreach (var token in tree.GetRoot().DescendantTokens())
        if (token.IsKind(SyntaxKind.IdentifierToken))
            ids.Add(token.ValueText);

    var lowered = ids.Select(id => id.ToLowerInvariant()).Distinct().Where(id => id.Length >= 3).ToList();
    if (lowered.Count > 0)
        docIdentifierSets.Add((file, lowered));

    totalFiles++;
    if (totalFiles % 500 == 0)
        Console.Error.Write($"\r  Processed {totalFiles} files...");
}
Console.Error.WriteLine($"\r  Processed {totalFiles} files, {docIdentifierSets.Count} with identifiers.     ");

// --- Analyze both hash functions per document ---

var crcDocBlooms = new List<int>();
var freqDocBlooms = new List<int>();
var crcDocDistincts = new List<int>();
var freqDocDistincts = new List<int>();

var globalCrcAllLengthDist = new Dictionary<int, long>();
var globalFreqAllLengthDist = new Dictionary<int, long>();
var globalCrcCoverLengthDist = new Dictionary<int, long>();
var globalFreqCoverLengthDist = new Dictionary<int, long>();
long globalCrcCoveringCount = 0, globalFreqCoveringCount = 0;

foreach (var (file, lowered) in docIdentifierSets)
{
    var crcSet = new HashSet<string>();
    var freqSet = new HashSet<string>();

    foreach (var id in lowered)
    {
        foreach (var (s, l) in BuildAllNgrams(id, CRC))
        {
            crcSet.Add(id.Substring(s, l));
            globalCrcAllLengthDist[l] = globalCrcAllLengthDist.GetValueOrDefault(l) + 1;
        }
        foreach (var (s, l) in BuildAllNgrams(id, FREQ))
        {
            freqSet.Add(id.Substring(s, l));
            globalFreqAllLengthDist[l] = globalFreqAllLengthDist.GetValueOrDefault(l) + 1;
        }
        foreach (var (s, l) in BuildCoveringNgrams(id, CRC))
        {
            globalCrcCoveringCount++;
            globalCrcCoverLengthDist[l] = globalCrcCoverLengthDist.GetValueOrDefault(l) + 1;
        }
        foreach (var (s, l) in BuildCoveringNgrams(id, FREQ))
        {
            globalFreqCoveringCount++;
            globalFreqCoverLengthDist[l] = globalFreqCoverLengthDist.GetValueOrDefault(l) + 1;
        }
    }

    crcDocBlooms.Add(BloomBytes(crcSet.Count));
    freqDocBlooms.Add(BloomBytes(freqSet.Count));
    crcDocDistincts.Add(crcSet.Count);
    freqDocDistincts.Add(freqSet.Count);
}

crcDocBlooms.Sort();
freqDocBlooms.Sort();
crcDocDistincts.Sort();
freqDocDistincts.Sort();

