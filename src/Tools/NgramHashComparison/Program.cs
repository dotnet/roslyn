sb.AppendLine();
sb.AppendLine("| Length | CRC count | CRC % | Freq count | Freq % | Shift |");
sb.AppendLine("|---|---|---|---|---|---|");

var allLens = globalCrcAllLengthDist.Keys.Union(globalFreqAllLengthDist.Keys).OrderBy(x => x).ToList();
foreach (var len in allLens.Where(l => l <= 15))
{
    var cc = globalCrcAllLengthDist.GetValueOrDefault(len);
    var fc = globalFreqAllLengthDist.GetValueOrDefault(len);
    sb.AppendLine($"| {len} | {cc:N0} | {100.0 * cc / totalCrcAll:F1}% | {fc:N0} | {100.0 * fc / totalFreqAll:F1}% | {100.0 * fc / totalFreqAll - 100.0 * cc / totalCrcAll:+0.0;-0.0}pp |");
}

var crcLong = allLens.Where(l => l > 15).Sum(l => globalCrcAllLengthDist.GetValueOrDefault(l));
var freqLong = allLens.Where(l => l > 15).Sum(l => globalFreqAllLengthDist.GetValueOrDefault(l));
sb.AppendLine($"| 16+ | {crcLong:N0} | {100.0 * crcLong / totalCrcAll:F1}% | {freqLong:N0} | {100.0 * freqLong / totalFreqAll:F1}% | {100.0 * freqLong / totalFreqAll - 100.0 * crcLong / totalCrcAll:+0.0;-0.0}pp |");

// --- Section 4: Query-time covering n-grams ---

sb.AppendLine();
sb.AppendLine("## Query-time covering n-grams (BuildCoveringNgrams)");
sb.AppendLine();
sb.AppendLine("This is what matters for query performance: fewer, longer covering n-grams = fewer");
sb.AppendLine("Bloom filter lookups, each more selective.");
sb.AppendLine();
sb.AppendLine($"Total covering n-grams: {globalCrcCoveringCount:N0} (CRC) vs {globalFreqCoveringCount:N0} (Freq), {100.0 * (globalFreqCoveringCount - globalCrcCoveringCount) / globalCrcCoveringCount:+0.0;-0.0}%");
sb.AppendLine();
sb.AppendLine("| Length | CRC count | CRC % | Freq count | Freq % | Shift |");
sb.AppendLine("|---|---|---|---|---|---|");

var coverLens = globalCrcCoverLengthDist.Keys.Union(globalFreqCoverLengthDist.Keys).OrderBy(x => x).ToList();
foreach (var len in coverLens.Where(l => l <= 15))
{
    var cc = globalCrcCoverLengthDist.GetValueOrDefault(len);
    var fc = globalFreqCoverLengthDist.GetValueOrDefault(len);
    sb.AppendLine($"| {len} | {cc:N0} | {100.0 * cc / globalCrcCoveringCount:F1}% | {fc:N0} | {100.0 * fc / globalFreqCoveringCount:F1}% | {100.0 * fc / globalFreqCoveringCount - 100.0 * cc / globalCrcCoveringCount:+0.0;-0.0}pp |");
}

crcLong = coverLens.Where(l => l > 15).Sum(l => globalCrcCoverLengthDist.GetValueOrDefault(l));
freqLong = coverLens.Where(l => l > 15).Sum(l => globalFreqCoverLengthDist.GetValueOrDefault(l));
if (crcLong > 0 || freqLong > 0)
    sb.AppendLine($"| 16+ | {crcLong:N0} | {100.0 * crcLong / globalCrcCoveringCount:F1}% | {freqLong:N0} | {100.0 * freqLong / globalFreqCoveringCount:F1}% | |");

var crcMeanCover = coverLens.Sum(l => l * globalCrcCoverLengthDist.GetValueOrDefault(l)) / (double)globalCrcCoveringCount;
var freqMeanCover = coverLens.Sum(l => l * globalFreqCoverLengthDist.GetValueOrDefault(l)) / (double)globalFreqCoveringCount;
sb.AppendLine();
sb.AppendLine($"**Mean covering n-gram length:** {crcMeanCover:F2} chars (CRC) vs {freqMeanCover:F2} chars (Freq)");

// --- Section 5: Selectivity ---

sb.AppendLine();
sb.AppendLine("## Selectivity analysis");
sb.AppendLine();
sb.AppendLine("Longer covering n-grams eliminate more false-positive documents.");
sb.AppendLine();
sb.AppendLine("| Metric | CRC | Freq | Delta |");
sb.AppendLine("|---|---|---|---|");

long CountCover(int minLen, Dictionary<int, long> dist) => dist.Where(kv => kv.Key >= minLen).Sum(kv => kv.Value);

var crcSel5 = CountCover(5, globalCrcCoverLengthDist);
var freqSel5 = CountCover(5, globalFreqCoverLengthDist);
var crcSel7 = CountCover(7, globalCrcCoverLengthDist);
var freqSel7 = CountCover(7, globalFreqCoverLengthDist);

sb.AppendLine($"| Covering length >= 5 | {crcSel5:N0} ({100.0 * crcSel5 / globalCrcCoveringCount:F1}%) | {freqSel5:N0} ({100.0 * freqSel5 / globalFreqCoveringCount:F1}%) | {100.0 * freqSel5 / globalFreqCoveringCount - 100.0 * crcSel5 / globalCrcCoveringCount:+0.0;-0.0}pp |");
sb.AppendLine($"| Covering length >= 7 | {crcSel7:N0} ({100.0 * crcSel7 / globalCrcCoveringCount:F1}%) | {freqSel7:N0} ({100.0 * freqSel7 / globalFreqCoveringCount:F1}%) | {100.0 * freqSel7 / globalFreqCoveringCount - 100.0 * crcSel7 / globalCrcCoveringCount:+0.0;-0.0}pp |");
sb.AppendLine($"| Mean covering length | {crcMeanCover:F2} | {freqMeanCover:F2} | {freqMeanCover - crcMeanCover:+0.00;-0.00} |");

// --- Section 6: Bigram weight analysis ---

sb.AppendLine();
sb.AppendLine("## Bigram weight analysis");
sb.AppendLine();
sb.AppendLine("The frequency hash assigns weight 0 to the most common bigram and weight 1368 to the rarest.");
sb.AppendLine("Higher weight = rarer bigram = more likely to be a stack peak (n-gram boundary).");
sb.AppendLine();

var bigramWeights = new List<(string bigram, uint weight)>();
for (var i = 0; i < AlphabetSize; i++)
    for (var j = 0; j < AlphabetSize; j++)
        bigramWeights.Add(($"{Alphabet[i]}{Alphabet[j]}", FrequencyWeights[i * AlphabetSize + j]));
bigramWeights.Sort((a, b) => a.weight.CompareTo(b.weight));

sb.AppendLine("**20 most common bigrams** (lowest weight — stay interior to n-grams):");
sb.AppendLine();
sb.AppendLine("| Rank | Bigram | Weight |");
sb.AppendLine("|---|---|---|");
for (var i = 0; i < 20; i++)
    sb.AppendLine($"| {i + 1} | `{bigramWeights[i].bigram}` | {bigramWeights[i].weight} |");

sb.AppendLine();
sb.AppendLine("**20 rarest bigrams** (highest weight — become n-gram boundaries):");
sb.AppendLine();
sb.AppendLine("| Rank | Bigram | Weight |");
sb.AppendLine("|---|---|---|");
for (var i = 0; i < 20; i++)
{
    var idx = bigramWeights.Count - 1 - i;
    sb.AppendLine($"| {i + 1} | `{bigramWeights[idx].bigram}` | {bigramWeights[idx].weight} |");
}

// --- Section 7: Sample identifiers ---

sb.AppendLine();
sb.AppendLine("## Detailed identifier breakdown");
sb.AppendLine();
sb.AppendLine("Shows where each hash function places covering n-gram boundaries. `|` marks a boundary.");
sb.AppendLine();

var samples = new[]
{
    "stringbuilder", "tostring", "cancellationtoken", "getdefaultvalue",
    "xmldocumentwriter", "iasyncenumerable", "trygetvalue", "addrange",
    "argumentnullexception", "diagnosticanalyzer"
};

foreach (var id in samples)
{
    var crcCover = BuildCoveringNgrams(id, CRC);
    var freqCover = BuildCoveringNgrams(id, FREQ);

    var crcBounds = new HashSet<int>(crcCover.Where(r => r.start > 0).Select(r => r.start));
    var freqBounds = new HashSet<int>(freqCover.Where(r => r.start > 0).Select(r => r.start));

    var crcMarked = string.Concat(id.Select((c, i) => crcBounds.Contains(i) ? $"|{c}" : $"{c}"));
    var freqMarked = string.Concat(id.Select((c, i) => freqBounds.Contains(i) ? $"|{c}" : $"{c}"));

    var crcStrs = crcCover.Select(r => $"`{id.Substring(r.start, r.length)}`");
    var freqStrs = freqCover.Select(r => $"`{id.Substring(r.start, r.length)}`");

    sb.AppendLine($"- **`{id}`**");
    sb.AppendLine($"  - CRC:  `{crcMarked}` → {string.Join(", ", crcStrs)} ({crcCover.Count})");
    sb.AppendLine($"  - Freq: `{freqMarked}` → {string.Join(", ", freqStrs)} ({freqCover.Count})");
}

