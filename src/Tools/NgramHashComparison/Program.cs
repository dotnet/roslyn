// Compares CRC-style hash vs frequency-based hash for the sparse n-gram algorithm.
// Runs both hash functions on the same Roslyn identifier corpus and reports:
//   - Per-document Bloom filter sizes (both functions)
//   - Per-document distinct n-gram counts
//   - N-gram length distributions (both BuildAllNgrams and BuildCoveringNgrams)
//   - Boundary placement analysis (which bigrams trigger breaks)
//   - Detailed per-identifier covering set comparisons
//
// Usage: NgramHashComparison <root-directory>

