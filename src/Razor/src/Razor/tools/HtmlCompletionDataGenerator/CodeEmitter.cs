    /// <summary>
    /// Collects all unique value arrays across all attributes and assigns field names.
    /// Returns two maps: shared (count > 1) and unique (count == 1).
    /// Uses the XSD simpleType name when available, then the attribute name, then indexed fallback.
    /// </summary>
    private static (Dictionary<string, string> Shared, Dictionary<string, string> Unique) BuildValueArrayMaps(HtmlSchema schema)
    {
        // key = joined values, value = (count, candidate name from simpleType or attribute)
        var valueSets = new Dictionary<string, (int Count, string? TypeName, string? AttrName)>(StringComparer.Ordinal);

        void CountValues(IEnumerable<AttributeData> attrs)
        {
            foreach (var attr in attrs)
            {
                if (attr.Values.Count > 0)
                {
                    var key = string.Join("|", attr.Values);
                    valueSets.TryGetValue(key, out var entry);
                    // Keep the first non-null type name or attribute name we encounter
                    var typeName = entry.TypeName ?? attr.ValueTypeName;
                    var attrName = entry.AttrName ?? (attr.ValueTypeName == null ? attr.Name : null);
                    valueSets[key] = (entry.Count + 1, typeName, attrName);
                }
            }
        }

        CountValues(schema.GlobalAttributes);
        foreach (var element in schema.Elements)
        {
            CountValues(element.Attributes);
        }

        var shared = new Dictionary<string, string>(StringComparer.Ordinal);
        var unique = new Dictionary<string, string>(StringComparer.Ordinal);
        var usedNames = new HashSet<string>(StringComparer.Ordinal);
        var fallbackIndex = 0;

        foreach (var (key, (count, typeName, attrName)) in valueSets.OrderBy(kv => kv.Key, StringComparer.Ordinal))
        {
            var fieldName = DeriveFieldName(typeName, attrName, usedNames, ref fallbackIndex);
            if (count > 1)
            {
                shared[key] = fieldName;
            }
            else
            {
                unique[key] = fieldName;
            }
        }

        return (shared, unique);
    }



    private static string DeriveFieldName(string? typeName, string? attrName, HashSet<string> usedNames, ref int fallbackIndex)
    {
        // Try simpleType name first: "inputTypeType" → "InputType"
        if (typeName != null)
        {
            var baseName = typeName;
            if (baseName.EndsWith("Type", StringComparison.Ordinal))
            {
                baseName = baseName[..^4];
            }

            var candidate = ToPascalCase(baseName);
            if (usedNames.Add(candidate))
            {
                return candidate;
            }
        }

        // Try attribute name: "aria-checked" → "AriaChecked"
        if (attrName != null)
        {
            var candidate = ToPascalCase(string.Concat(attrName.Split('-').Select((part, i) =>
                i == 0 ? part : char.ToUpperInvariant(part[0]) + part[1..])));
            if (usedNames.Add(candidate))
            {
                return candidate;
            }
        }

        // Fallback for collisions
        string fallback;
        do
        {
            fallback = $"Values{fallbackIndex++}";
        } while (!usedNames.Add(fallback));

        return fallback;
    }

    private static void EmitValueArrayFields(StringBuilder sb, Dictionary<string, string> valueArrayMap, string indent = "    ")
    {
        if (valueArrayMap.Count == 0) return;

        foreach (var (key, fieldName) in valueArrayMap.OrderBy(kv => kv.Value, StringComparer.Ordinal))
        {
            var values = key.Split('|');
            sb.AppendLine($"{indent}internal static readonly ImmutableArray<string> {fieldName} = [{string.Join(", ", values.Select(EscapeString))}];");
        }
    }

    /// <summary>
    /// Builds a map of attribute instance expressions that appear more than once → field name.
    /// </summary>
    private static Dictionary<string, string> BuildAttrInstanceMap(HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap)
    {
        var instances = new Dictionary<string, int>(StringComparer.Ordinal);

        void Count(IEnumerable<AttributeData> attrs)
        {
            foreach (var attr in attrs)
            {
                var key = GetAttrInstanceKey(attr, schema, sharedValueMap, uniqueValueMap);
                instances.TryGetValue(key, out var count);
                instances[key] = count + 1;
            }
        }

        Count(schema.GlobalAttributes);
        foreach (var element in schema.Elements)
        {
            Count(element.Attributes);
        }

        var map = new Dictionary<string, string>(StringComparer.Ordinal);
        var usedNames = new HashSet<string>(StringComparer.Ordinal);

        foreach (var (key, count) in instances.Where(kv => kv.Value > 1).OrderBy(kv => kv.Key, StringComparer.Ordinal))
        {
            // Derive field name from the attribute name embedded in the key
            var attrName = key.Split('|')[0]; // first segment is the attribute name
            var camelCase = string.Concat(attrName.Split('-').Select((part, i) =>
                i == 0 ? part : char.ToUpperInvariant(part[0]) + part[1..]));
            var candidate = ToPascalCase(camelCase);
            if (!usedNames.Add(candidate))
            {
                var idx = 2;
                while (!usedNames.Add($"{candidate}{idx}")) idx++;
                candidate = $"{candidate}{idx}";
            }
            map[key] = candidate;
        }

        return map;
    }

    private static string GetAttrInstanceKey(AttributeData attr, HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap)
    {
        var url = GetUrl(attr.DescriptionId, schema);
        var valuesExpr = GetValuesExpression(attr, sharedValueMap, uniqueValueMap);
        // key format: name|url|valuesExpr|boolean|event|icon|hasExternalCompletion|baseline|baselineDate
        return $"{attr.Name}|{url}|{valuesExpr}|{attr.IsBoolean}|{attr.IsEvent}|{attr.Icon ?? ""}|{attr.HasExternalCompletion}|{attr.Baseline}|{attr.BaselineDate}";
    }

    private static string GetValuesExpression(AttributeData attr, Dictionary<string, string> sharedValueArrayMap, Dictionary<string, string> uniqueValueArrayMap)
    {
        if (attr.Values.Count == 0)
        {
            return "[]";
        }

        var key = string.Join("|", attr.Values);
        if (sharedValueArrayMap.TryGetValue(key, out var sharedFieldName))
        {
            return $"HtmlAttributeValueGroups.Shared.{sharedFieldName}";
        }

        if (uniqueValueArrayMap.TryGetValue(key, out var uniqueFieldName))
        {
            return $"HtmlAttributeValueGroups.Unique.{uniqueFieldName}";
        }

        return $"[{string.Join(", ", attr.Values.Select(EscapeString))}]";
    }

    private static void EmitSharedAttrInstances(StringBuilder sb, Dictionary<string, string> attrInstanceMap)
    {
        if (attrInstanceMap.Count == 0) return;

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Shared attribute instances referenced by multiple elements, deduplicated for reduced allocations.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("internal static partial class HtmlAttributes");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class Shared");
        sb.AppendLine("    {");
        foreach (var (key, fieldName) in attrInstanceMap.OrderBy(kv => kv.Value, StringComparer.Ordinal))
        {
            var parts = key.Split('|');
            var name = parts[0];
            var url = parts[1];
            var valuesExpr = parts[2];
            var isBoolean = bool.Parse(parts[3]);
            var isEvent = bool.Parse(parts[4]);
            var icon = parts.Length > 5 ? parts[5] : "";
            var hasExternalCompletion = parts.Length > 6 && bool.Parse(parts[6]);
            var baseline = parts.Length > 7 ? parts[7] : "";
            var baselineDate = parts.Length > 8 ? parts[8] : "";

            sb.AppendLine($"        internal static readonly HtmlAttributeInfo {fieldName} = new({BuildAttrCtorArgs(name, url, baseline, baselineDate, valuesExpr, isBoolean, isEvent, icon, hasExternalCompletion)});");
        }
        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

    /// <summary>
    /// Builds a map from (elementName, attrInstanceKey) → field name for attributes that appear only once.
    /// </summary>
    private static Dictionary<string, string> BuildUniqueAttrMap(HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap, Dictionary<string, string> attrInstanceMap)
    {
        var map = new Dictionary<string, string>(StringComparer.Ordinal);
        var usedNames = new HashSet<string>(StringComparer.Ordinal);

        void Process(string ownerName, IEnumerable<AttributeData> attrs)
        {
            foreach (var attr in attrs)
            {
                var instanceKey = GetAttrInstanceKey(attr, schema, sharedValueMap, uniqueValueMap);
                if (attrInstanceMap.ContainsKey(instanceKey))
                    continue; // already shared

                var compositeKey = $"{ownerName}|{instanceKey}";
                if (map.ContainsKey(compositeKey))
                    continue;

                var attrCamel = string.Concat(attr.Name.Split('-').Select((part, i) =>
                    i == 0 ? part : char.ToUpperInvariant(part[0]) + part[1..]));
                var ownerCamel = string.Concat(ownerName.Split('-').Select((part, i) =>
                    i == 0 ? part : char.ToUpperInvariant(part[0]) + part[1..].Replace("-", "")));
                var candidate = $"{ToPascalCase(ownerCamel)}_{ToPascalCase(attrCamel)}";
                if (!usedNames.Add(candidate))
                {
                    var idx = 2;
                    while (!usedNames.Add($"{candidate}{idx}")) idx++;
                    candidate = $"{candidate}{idx}";
                }
                map[compositeKey] = candidate;
            }
        }

        Process("global", schema.GlobalAttributes);
        foreach (var element in schema.Elements)
        {
            Process(element.Name, element.Attributes);
        }

        return map;
    }

    private static void EmitUniqueAttrInstances(StringBuilder sb, HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap, Dictionary<string, string> uniqueAttrMap)
    {
        if (uniqueAttrMap.Count == 0) return;

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Attribute instances that are unique to a single element.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("internal static partial class HtmlAttributes");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class Unique");
        sb.AppendLine("    {");
        foreach (var (compositeKey, fieldName) in uniqueAttrMap.OrderBy(kv => kv.Value, StringComparer.Ordinal))
        {
            // compositeKey = "elementName|attrName|desc|url|valuesExpr|boolean|event|icon"
            var firstPipe = compositeKey.IndexOf('|');
            var instanceKey = compositeKey[(firstPipe + 1)..];
            var parts = instanceKey.Split('|');
            var name = parts[0];
            var url = parts[1];
            var valuesExpr = parts[2];
            var isBoolean = bool.Parse(parts[3]);
            var isEvent = bool.Parse(parts[4]);
            var icon = parts.Length > 5 ? parts[5] : "";
            var hasExternalCompletion = parts.Length > 6 && bool.Parse(parts[6]);
            var baseline = parts.Length > 7 ? parts[7] : "";
            var baselineDate = parts.Length > 8 ? parts[8] : "";

            sb.AppendLine($"        internal static readonly HtmlAttributeInfo {fieldName} = new({BuildAttrCtorArgs(name, url, baseline, baselineDate, valuesExpr, isBoolean, isEvent, icon, hasExternalCompletion)});");
        }
        sb.AppendLine("    }");
        sb.AppendLine("}");
    }

