    /// <summary>
    /// Returns a camelCase identifier from a hyphenated or camelCase name.
    /// "flowContent" → "flowContent", "phrasing-content" → "phrasingContent"
    /// </summary>
    private static string CamelCase(string name)
    {
        if (!name.Contains('-'))
            return name;
        return string.Concat(name.Split('-').Select((part, i) =>
            i == 0 ? part : char.ToUpperInvariant(part[0]) + part[1..]));
    }

    private static string ToPascalCase(string name)
    {
        if (string.IsNullOrEmpty(name))
            return name;
        return char.ToUpperInvariant(name[0]) + name[1..];
    }

    private static string BoolLiteral(bool value) => value ? "true" : "false";

    /// <summary>
    /// Builds a map from element name → attribute array field name for elements with attributes.
    /// Detects shared arrays (elements with identical attribute array content) vs unique ones.
    /// Returns (shared map: key→fieldName, unique map: elementName→fieldName, keyMap: elementName→key).
    /// </summary>
    private static (Dictionary<string, string> Shared, Dictionary<string, string> Unique) BuildElementAttrArrayMap(
        HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap,
        Dictionary<string, string> attrInstanceMap, Dictionary<string, string> uniqueAttrMap)
    {
        // Build a content key for each element's attribute array
        var elementKeys = new Dictionary<string, string>(StringComparer.Ordinal); // elementName → key
        var keyCounts = new Dictionary<string, int>(StringComparer.Ordinal); // key → count

        foreach (var element in schema.Elements)
        {
            if (element.Attributes.Count == 0)
                continue;

            var key = BuildElementAttrKey(element, schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap);
            elementKeys[element.Name] = key;
            keyCounts.TryGetValue(key, out var count);
            keyCounts[key] = count + 1;
        }

        // Build shared (count > 1) and unique (count == 1) maps
        var shared = new Dictionary<string, string>(StringComparer.Ordinal); // key → fieldName
        var unique = new Dictionary<string, string>(StringComparer.Ordinal); // elementName → fieldName
        var sharedNames = new HashSet<string>(StringComparer.Ordinal);

        foreach (var element in schema.Elements.OrderBy(e => e.Name, StringComparer.OrdinalIgnoreCase))
        {
            if (!elementKeys.TryGetValue(element.Name, out var key))
                continue;

            if (keyCounts[key] > 1)
            {
                if (!shared.ContainsKey(key))
                {
                    // Name from first element alphabetically
                    shared[key] = ToPascalCase(CamelCase(element.Name));
                }
            }
            else
            {
                unique[element.Name] = ToPascalCase(CamelCase(element.Name));
            }
        }

        return (shared, unique);
    }

    /// <summary>
    /// Builds a string key representing the ordered attribute references for an element.
    /// </summary>
    private static string BuildElementAttrKey(ElementData element, HtmlSchema schema,
        Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap,
        Dictionary<string, string> attrInstanceMap, Dictionary<string, string> uniqueAttrMap)
    {
        var parts = new List<string>();
        foreach (var attr in element.Attributes.OrderBy(a => a.Name, StringComparer.OrdinalIgnoreCase))
        {
            var instanceKey = GetAttrInstanceKey(attr, schema, sharedValueMap, uniqueValueMap);
            if (attrInstanceMap.TryGetValue(instanceKey, out var sharedFieldName))
            {
                parts.Add($"S:{sharedFieldName}");
            }
            else
            {
                var compositeKey = $"{element.Name}|{instanceKey}";
                if (uniqueAttrMap.TryGetValue(compositeKey, out var uniqueFieldName))
                {
                    parts.Add($"U:{uniqueFieldName}");
                }
                else
                {
                    parts.Add($"I:{instanceKey}");
                }
            }
        }
        return string.Join(";", parts);
    }

    /// <summary>
    /// Emits the shared attribute groups file (element attribute arrays used by multiple elements).
    /// </summary>
    public static string EmitAttributeGroupsShared(HtmlSchema schema)
    {
        var sb = new StringBuilder(32 * 1024);

        EmitHeader(sb);

        var (sharedValueMap, uniqueValueMap) = BuildValueArrayMaps(schema);
        var attrInstanceMap = BuildAttrInstanceMap(schema, sharedValueMap, uniqueValueMap);
        var uniqueAttrMap = BuildUniqueAttrMap(schema, sharedValueMap, uniqueValueMap, attrInstanceMap);
        var (sharedElemAttrs, _) = BuildElementAttrArrayMap(schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap);

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Element attribute arrays shared by multiple elements with identical attribute sets.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("internal static partial class HtmlAttributeGroups");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class Shared");
        sb.AppendLine("    {");

        // Global attributes (valid on all HTML elements)
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Attributes valid on all HTML elements (core + events + microdata + ARIA).");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        internal static readonly ImmutableArray<HtmlAttributeInfo> Global =");
        sb.AppendLine("        [");

        foreach (var attr in schema.GlobalAttributes.OrderBy(a => a.Name, StringComparer.OrdinalIgnoreCase))
        {
            EmitAttributeInfo(sb, attr, "global", schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap, "            ");
        }

        sb.AppendLine("        ];");
        sb.AppendLine();

        foreach (var (key, fieldName) in sharedElemAttrs.OrderBy(kv => kv.Value, StringComparer.Ordinal))
        {
            EmitElementAttrField(sb, key, fieldName, "        ");
        }

        TrimTrailingBlankLine(sb);
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    /// <summary>
    /// Emits the unique attribute groups file (element attribute arrays unique to a single element).
    /// </summary>
    public static string EmitAttributeGroupsUnique(HtmlSchema schema)
    {
        var sb = new StringBuilder(64 * 1024);

        EmitHeader(sb);

        var (sharedValueMap, uniqueValueMap) = BuildValueArrayMaps(schema);
        var attrInstanceMap = BuildAttrInstanceMap(schema, sharedValueMap, uniqueValueMap);
        var uniqueAttrMap = BuildUniqueAttrMap(schema, sharedValueMap, uniqueValueMap, attrInstanceMap);
        var (_, uniqueElemAttrs) = BuildElementAttrArrayMap(schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap);

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Element attribute arrays unique to a single element.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("internal static partial class HtmlAttributeGroups");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class Unique");
        sb.AppendLine("    {");

        foreach (var (elementName, fieldName) in uniqueElemAttrs.OrderBy(kv => kv.Value, StringComparer.Ordinal))
        {
            var element = schema.Elements.First(e => e.Name == elementName);
            var key = BuildElementAttrKey(element, schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap);
            EmitElementAttrField(sb, key, fieldName, "        ");
        }

        TrimTrailingBlankLine(sb);
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    /// <summary>
    /// Emits a single element attribute array field from its key.
    /// </summary>
    private static void EmitElementAttrField(StringBuilder sb, string key, string fieldName, string indent)
    {
        var parts = key.Split(';');
        sb.AppendLine($"{indent}internal static readonly ImmutableArray<HtmlAttributeInfo> {fieldName} =");
        sb.AppendLine($"{indent}[");
        foreach (var part in parts)
        {
            var prefix = part[..2];
            var value = part[2..];
            switch (prefix)
            {
                case "S:":
                    sb.AppendLine($"{indent}    HtmlAttributes.Shared.{value},");
                    break;
                case "U:":
                    sb.AppendLine($"{indent}    HtmlAttributes.Unique.{value},");
                    break;
                case "I:":
                    // Inline fallback — reconstruct from instance key
                    var attrParts = value.Split('|');
                    var name = attrParts[0];
                    var url = attrParts[1];
                    var valuesExpr = attrParts[2];
                    var isBoolean = bool.Parse(attrParts[3]);
                    var isEvent = bool.Parse(attrParts[4]);
                    var icon = attrParts.Length > 5 ? attrParts[5] : "";
                    var hasExternalCompletion = attrParts.Length > 6 && bool.Parse(attrParts[6]);
                    var baseline = attrParts.Length > 7 ? attrParts[7] : "";
                    var baselineDate = attrParts.Length > 8 ? attrParts[8] : "";
                    sb.AppendLine($"{indent}    new({BuildAttrCtorArgs(name, url, baseline, baselineDate, valuesExpr, isBoolean, isEvent, icon, hasExternalCompletion)}),");
                    break;
            }
        }
        sb.AppendLine($"{indent}];");
        sb.AppendLine();
    }

    /// <summary>
    /// Gets the element attribute array field reference expression for use in EmitElementInfo.
    /// Returns null if the element has no attributes.
    /// </summary>
    internal static string? GetElementAttrExpression(ElementData element, HtmlSchema schema,
        Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap,
        Dictionary<string, string> attrInstanceMap, Dictionary<string, string> uniqueAttrMap,
        Dictionary<string, string> sharedElemAttrMap, Dictionary<string, string> uniqueElemAttrMap)
    {
        if (element.Attributes.Count == 0)
            return null;

        var key = BuildElementAttrKey(element, schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap);
        if (sharedElemAttrMap.TryGetValue(key, out var sharedField))
            return $"HtmlAttributeGroups.Shared.{sharedField}";

        if (uniqueElemAttrMap.TryGetValue(element.Name, out var uniqueField))
            return $"HtmlAttributeGroups.Unique.{uniqueField}";

        return null; // shouldn't happen
    }

    /// <summary>
    /// Removes a trailing blank line from the StringBuilder (the newline before the last newline).
    /// Used to prevent empty lines between the last field and the closing brace.
    /// </summary>
    private static void TrimTrailingBlankLine(StringBuilder sb)
    {
        // StringBuilder ends with "...last line\r\n\r\n" — remove one "\r\n"
        if (sb.Length >= 4
            && sb[sb.Length - 1] == '\n'
            && sb[sb.Length - 2] == '\r'
            && sb[sb.Length - 3] == '\n'
            && sb[sb.Length - 4] == '\r')
        {
            sb.Length -= 2;
        }
        else if (sb.Length >= 2
            && sb[sb.Length - 1] == '\n'
            && sb[sb.Length - 2] == '\n')
        {
            sb.Length -= 1;
        }
    }
