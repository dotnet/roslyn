    public static string EmitHtmlElements(HtmlSchema schema)
    {
        var sb = new StringBuilder(64 * 1024);

        EmitHeader(sb);

        var (shared, unique) = BuildValueArrayMaps(schema);
        var attrInstanceMap = BuildAttrInstanceMap(schema, shared, unique);
        var uniqueAttrMap = BuildUniqueAttrMap(schema, shared, unique, attrInstanceMap);
        var (sharedElemAttrs, uniqueElemAttrs) = BuildElementAttrArrayMap(schema, shared, unique, attrInstanceMap, uniqueAttrMap);
        var (sharedGroupMap, uniqueGroupMap) = BuildElementGroupMaps(schema);

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// All known HTML elements with their metadata, attributes, and allowed children.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("internal static class HtmlElements");
        sb.AppendLine("{");

        EmitElements(sb, schema, shared, unique, attrInstanceMap, uniqueAttrMap, sharedElemAttrs, uniqueElemAttrs, sharedGroupMap, uniqueGroupMap);

        sb.AppendLine("}");

        return sb.ToString();
    }

