    private static void EmitElementInfo(StringBuilder sb, ElementData element, HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap, Dictionary<string, string> attrInstanceMap, Dictionary<string, string> uniqueAttrMap, Dictionary<string, string> sharedElemAttrMap, Dictionary<string, string> uniqueElemAttrMap, Dictionary<string, string> sharedGroupMap, Dictionary<string, string> uniqueGroupMap)
    {
        var descId = element.DescriptionId;
        var url = GetUrl(descId, schema);
        var baseline = element.Baseline;
        var baselineDate = element.BaselineDate;
        var isTransparent = element.AllowedChildren.Count == 1 && element.AllowedChildren[0] == "*";
        var hasChildren = element.AllowedChildren.Count > 0 && !isTransparent;

        // Build the list of trailing optional parameters
        var trailingParams = new List<string>();
        if (baseline != "") trailingParams.Add($"baseline: {EscapeString(baseline)}");
        if (baselineDate != "") trailingParams.Add($"baselineDate: {EscapeString(baselineDate)}");
        if (hasChildren)
        {
            var childrenExpr = GetChildrenExpression(element, schema, sharedGroupMap, uniqueGroupMap);
            if (childrenExpr != null)
            {
                trailingParams.Add($"allowedChildren: {childrenExpr}");
            }
            else
            {
                // Fallback inline (should not happen with complete maps)
                var inlineChildren = string.Join(", ",
                    element.AllowedChildren.OrderBy(c => c, StringComparer.OrdinalIgnoreCase).Select(c => $"\"{c}\""));
                trailingParams.Add($"allowedChildren: [{inlineChildren}]");
            }
        }

        if (isTransparent) trailingParams.Add("isTransparentContent: true");
        if (element.HasCustomIcon) trailingParams.Add("kind: HtmlElementKind.Angular");
        if (!string.IsNullOrEmpty(element.DisallowedAncestors))
        {
            var ancestors = element.DisallowedAncestors!.Split(' ', StringSplitOptions.RemoveEmptyEntries);
            var ancestorList = string.Join(", ", ancestors.Select(a => $"\"{a}\""));
            trailingParams.Add($"disallowedAncestors: [{ancestorList}]");
        }
        if (element.IsImplicitlyClosed) trailingParams.Add("isImplicitlyClosed: true");
        if (element.HasExternalCompletion) trailingParams.Add("hasExternalCompletion: true");

        sb.AppendLine($"        new HtmlElementInfo(");
        sb.AppendLine($"            {EscapeString(element.Name)},");

        // Attributes parameter — use field reference if available, omit when empty
        var attrExpr = GetElementAttrExpression(element, schema, sharedValueMap, uniqueValueMap, attrInstanceMap, uniqueAttrMap, sharedElemAttrMap, uniqueElemAttrMap);

        if (attrExpr != null)
        {
            trailingParams.Insert(0, $"attributes: {attrExpr}");
        }

        if (trailingParams.Count == 0)
        {
            // Only name and url — close the constructor
            sb.AppendLine($"            {EscapeString(url)}),");
        }
        else
        {
            sb.AppendLine($"            {EscapeString(url)},");
            for (int i = 0; i < trailingParams.Count; i++)
            {
                var suffix = i == trailingParams.Count - 1 ? ")," : ",";
                sb.AppendLine($"            {trailingParams[i]}{suffix}");
            }
        }
    }

    private static void EmitAttributeInfo(StringBuilder sb, AttributeData attr, string ownerName, HtmlSchema schema, Dictionary<string, string> sharedValueMap, Dictionary<string, string> uniqueValueMap, Dictionary<string, string> attrInstanceMap, Dictionary<string, string> uniqueAttrMap, string indent)
    {
        // Check if this instance is shared (multi-element)
        var instanceKey = GetAttrInstanceKey(attr, schema, sharedValueMap, uniqueValueMap);
        if (attrInstanceMap.TryGetValue(instanceKey, out var sharedFieldName))
        {
            sb.AppendLine($"{indent}HtmlAttributes.Shared.{sharedFieldName},");
            return;
        }

        // Check if this instance is in the unique map
        var compositeKey = $"{ownerName}|{instanceKey}";
        if (uniqueAttrMap.TryGetValue(compositeKey, out var uniqueFieldName))
        {
            sb.AppendLine($"{indent}HtmlAttributes.Unique.{uniqueFieldName},");
            return;
        }

        // Fallback: inline (shouldn't happen if maps are complete)
        var url = GetUrl(attr.DescriptionId, schema);
        var baseline = attr.Baseline;
        var baselineDate = attr.BaselineDate;
        var valuesExpr = GetValuesExpression(attr, sharedValueMap, uniqueValueMap);

        sb.AppendLine($"{indent}new({BuildAttrCtorArgs(attr.Name, url, baseline, baselineDate, valuesExpr, attr.IsBoolean, attr.IsEvent, attr.Icon ?? "", attr.HasExternalCompletion)}),");
    }

    /// <summary>
    /// Builds the constructor argument string, omitting trailing default values.
    /// Order: name, documentationUrl="", baseline="", baselineDate="", values=default, isBoolean=false, kind=HtmlAttributeKind.Default
    /// </summary>
    private static string BuildAttrCtorArgs(string name, string url, string baseline, string baselineDate, string valuesExpr, bool isBoolean, bool isEvent, string icon, bool hasExternalCompletion)
    {
        var kind = isEvent ? "Event"
            : icon == "aria16.png" ? "Aria"
            : icon == "angular16.png" ? "Angular"
            : null;
        var args = new List<string> { EscapeString(name) };

        // After name (always positional), use named parameters for non-default values.
        var hasValues = valuesExpr != "[]";
        var hasUrl = url != "";

        if (hasUrl) args.Add($"documentationUrl: {EscapeString(url)}");
        if (baseline != "") args.Add($"baseline: {EscapeString(baseline)}");
        if (baselineDate != "") args.Add($"baselineDate: {EscapeString(baselineDate)}");
        if (hasValues) args.Add($"values: {valuesExpr}");
        if (isBoolean) args.Add("isBoolean: true");
        if (kind != null) args.Add($"kind: HtmlAttributeKind.{kind}");
        if (hasExternalCompletion) args.Add("hasExternalCompletion: true");

        return string.Join(", ", args);
    }

