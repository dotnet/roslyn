    {
        var sb = new StringBuilder(16 * 1024);

        EmitHeader(sb);

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Unique groups of valid attribute values (used by exactly one attribute).");
        sb.AppendLine("/// Derived from XSD simpleType enumerations.");
