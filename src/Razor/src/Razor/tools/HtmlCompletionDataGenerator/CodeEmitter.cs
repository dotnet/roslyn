    }

    public static string EmitSharedAttrs(HtmlSchema schema)
    {
        var sb = new StringBuilder(16 * 1024);

        EmitHeader(sb);

        var (shared, unique) = BuildValueArrayMaps(schema);
        var attrInstanceMap = BuildAttrInstanceMap(schema, shared, unique);
        EmitSharedAttrInstances(sb, attrInstanceMap);

        return sb.ToString();
    }

    public static string EmitUniqueAttrs(HtmlSchema schema)
    {
        var sb = new StringBuilder(32 * 1024);

        EmitHeader(sb);

        var (shared, unique) = BuildValueArrayMaps(schema);
        var attrInstanceMap = BuildAttrInstanceMap(schema, shared, unique);
        var uniqueAttrMap = BuildUniqueAttrMap(schema, shared, unique, attrInstanceMap);
        EmitUniqueAttrInstances(sb, schema, shared, unique, uniqueAttrMap);

        return sb.ToString();
    }

