    {
        var sb = new StringBuilder(16 * 1024);

        EmitHeader(sb);

        sb.AppendLine("/// <summary>");
        sb.AppendLine("/// Shared groups of valid attribute values (used by more than one attribute).");
        sb.AppendLine("/// Derived from XSD simpleType enumerations.");
        sb.AppendLine("/// </summary>");
        sb.AppendLine("internal static partial class HtmlAttributeValueGroups");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class Shared");
