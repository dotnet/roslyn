    /// <summary>
    /// Builds maps for element child group extraction.
    /// Shared: child arrays used by more than one element (including named content groups).
    /// Unique: child arrays used by exactly one element.
    /// </summary>
    private static (Dictionary<string, string> sharedMap, Dictionary<string, string> uniqueMap) BuildElementGroupMaps(HtmlSchema schema)
    {
        // First, build a key for each element's children (sorted child list as pipe-delimited string)
        var elementKeys = new Dictionary<string, string>(); // elementName → key
        var keyUsage = new Dictionary<string, List<string>>(); // key → list of element names

        foreach (var element in schema.Elements)
        {
            if (element.AllowedChildren.Count == 0)
                continue;
            if (element.AllowedChildren.Count == 1 && element.AllowedChildren[0] == "*")
                continue; // transparent

            var key = string.Join("|", element.AllowedChildren.OrderBy(c => c, StringComparer.OrdinalIgnoreCase));
            elementKeys[element.Name] = key;

            if (!keyUsage.TryGetValue(key, out var users))
            {
                users = new List<string>();
                keyUsage[key] = users;
            }
            users.Add(element.Name);
        }

        // Named content groups are always shared (they come from XSD group definitions)
        var namedGroupKeys = new Dictionary<string, string>(); // key → groupName
        foreach (var (groupName, groupChildren) in schema.ContentGroups)
        {
            var key = string.Join("|", groupChildren.OrderBy(c => c, StringComparer.OrdinalIgnoreCase));
            namedGroupKeys[key] = groupName;
        }

        // Shared: named groups + any key used by 2+ elements
        var sharedMap = new Dictionary<string, string>(); // key → fieldName
        foreach (var (key, groupName) in namedGroupKeys)
        {
            sharedMap[key] = ToPascalCase(groupName);
        }

        // Check for unnamed shared groups (same child list on 2+ elements, not a named group)
        var sharedIndex = 0;
        foreach (var (key, users) in keyUsage)
        {
            if (users.Count > 1 && !namedGroupKeys.ContainsKey(key))
            {
                sharedMap[key] = $"Shared{sharedIndex++}";
            }
        }

        // Unique: keys used by exactly one element and not matching a named group
        var uniqueMap = new Dictionary<string, string>(); // elementName → fieldName
        foreach (var (elementName, key) in elementKeys)
        {
            if (!sharedMap.ContainsKey(key))
            {
                uniqueMap[elementName] = ToPascalCase(elementName);
            }
        }

        return (sharedMap, uniqueMap);
    }

    /// <summary>
    /// Gets the children field expression for an element.
    /// Returns null if the element is void (no children) or transparent.
    /// </summary>
    private static string? GetChildrenExpression(ElementData element, HtmlSchema schema, Dictionary<string, string> sharedGroupMap, Dictionary<string, string> uniqueGroupMap)
    {
        if (element.AllowedChildren.Count == 1 && element.AllowedChildren[0] == "*")
            return null;

        if (element.AllowedChildren.Count == 0)
            return null;

        var key = string.Join("|", element.AllowedChildren.OrderBy(c => c, StringComparer.OrdinalIgnoreCase));

        if (sharedGroupMap.TryGetValue(key, out var sharedField))
            return $"HtmlElementGroups.Shared.{sharedField}";

        if (uniqueGroupMap.TryGetValue(element.Name, out var uniqueField))
            return $"HtmlElementGroups.Unique.{uniqueField}";

        return null;
    }

